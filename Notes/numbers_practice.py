# Operators that work on numbers
print(1 + 1)
print(1 + 1)
print(10 * 10)
print(9 / 3)

# Can use underscore to serperate numbers (like commas in numbers)
print(123_456_789)

# Since there is division, answer will be a float
print(1 + 3 / 1 + 5) # Follows order of operations (BEDMAS)

print(2 ** 10)       # Raises to a power (2^10)

print(9 //2)         # floor division (putting two division signs), 
# gets rid of decimals, rounds DOWN to the nearest numbers
print(9 % 2)         # modulo (gives you remainder)
